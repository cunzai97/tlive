#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';

const ROOT = resolve(dirname(new URL(import.meta.url).pathname), '..');
const SUMMARY_PATH = join(ROOT, 'reports', 'coverage-e2e', 'coverage-summary.json');
const OUT_DIR = join(ROOT, 'reports', 'debt');
const OUT_PATH = join(OUT_DIR, 'blackbox-coverage.md');

function pct(metric) {
  return typeof metric?.pct === 'number' ? metric.pct : 0;
}

function mdTable(headers, rows) {
  if (!rows.length) return '_None._\n';
  return [
    `| ${headers.join(' | ')} |`,
    `| ${headers.map(() => '---').join(' | ')} |`,
    ...rows.map((row) => `| ${row.map((cell) => String(cell).replace(/\n/g, '<br>')).join(' | ')} |`),
    '',
  ].join('\n');
}

if (!existsSync(SUMMARY_PATH)) {
  console.error(`Missing coverage summary: ${relative(ROOT, SUMMARY_PATH)}`);
  console.error('Run npm run test:e2e:coverage first.');
  process.exit(1);
}

const summary = JSON.parse(readFileSync(SUMMARY_PATH, 'utf-8'));
const rows = Object.entries(summary)
  .filter(([file]) => file !== 'total')
  .map(([file, metrics]) => ({
    file: relative(ROOT, file),
    statements: pct(metrics.statements),
    branches: pct(metrics.branches),
    functions: pct(metrics.functions),
    lines: pct(metrics.lines),
  }))
  .filter((entry) => entry.file.startsWith('src/') && !entry.file.includes('/__tests__/'))
  .sort((a, b) => a.statements - b.statements || a.file.localeCompare(b.file));

const zeroRows = rows
  .filter((entry) => entry.statements === 0)
  .map((entry) => [entry.file, `${entry.functions}%`, `${entry.branches}%`, `${entry.lines}%`]);

const lowRows = rows
  .filter((entry) => entry.statements > 0 && entry.statements < 20)
  .map((entry) => [
    entry.file,
    `${entry.statements}%`,
    `${entry.functions}%`,
    `${entry.branches}%`,
    `${entry.lines}%`,
  ]);

const total = summary.total;
const content = `# Black-box E2E Coverage Audit

Generated by \`npm run test:e2e:coverage\`.

This report is a deletion-candidate finder, not proof of dead code. A file that is 0% covered by black-box E2E may still be an entrypoint, setup path, rare error path, or provider-specific integration. Use it together with \`npm run lint:dead\` and \`npm run audit:debt\`.

## Total

${mdTable(
  ['Statements', 'Branches', 'Functions', 'Lines'],
  [[`${pct(total.statements)}%`, `${pct(total.branches)}%`, `${pct(total.functions)}%`, `${pct(total.lines)}%`]],
)}

## Zero-covered production files

${mdTable(['File', 'Functions', 'Branches', 'Lines'], zeroRows)}

## Low-covered production files

${mdTable(['File', 'Statements', 'Functions', 'Branches', 'Lines'], lowRows)}

## Review rule

\`0% black-box coverage + weak/static-only references + no active product path\` is a strong refactor or deletion candidate.
`;

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(OUT_PATH, content);

console.log(`Black-box coverage audit written to ${relative(ROOT, OUT_PATH)}`);
console.log(`- zero-covered production files: ${zeroRows.length}`);
console.log(`- low-covered production files: ${lowRows.length}`);
